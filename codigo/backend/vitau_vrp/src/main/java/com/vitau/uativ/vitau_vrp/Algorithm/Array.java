package com.vitau.uativ.vitau_vrp.Algorithm;

public class Array {
    private int id;
    private boolean isFuncionario;

    public Array(int id, boolean isFuncionario) {
        this.id = id;
        this.isFuncionario = isFuncionario;
    }

    public int getId() {
        return id;
    }

    public boolean isFuncionario() {
        return isFuncionario;
    }
}
